namespace Sudoku.Concepts;

/// <summary>
/// Represents a type of rotation.
/// </summary>
public enum RotationType
{
	/// <summary>
	/// Represents no rotation.
	/// </summary>
	None = 0,

	/// <summary>
	/// Represents single rotation.
	/// </summary>
	Single,

	/// <summary>
	/// Represents double rotation.
	/// </summary>
	Double,

	/// <summary>
	/// Represents triple rotation.
	/// </summary>
	Triple
}
